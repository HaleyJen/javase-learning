public class TransferMean {
    public static void main(String[] args) {
        //1 输出引号  I am "kelly"
        System.out.println("I am \"kelly\"");

        //2 输出反斜杠 C:\hello.txt
        System.out.println("C:\\hello.txt");

        //3 换行 \n
        System.out.println("hello\nworld!");

        //4 制表符 \t 表格对齐
        System.out.println("Name\tAge\tScore\nalice\t20\t95");


    }
}
